#!/usr/bin/perl

# Hashes are like maps
# Used with % sign

%user_data = ('Danish',23,'Raj',78,'Him',67); #Enter data with key,value
%hash1 = (key1 => 'val1', key2=>'val2');  #Enter data with key=>val
print "\$user_data{'Danish'} = $user_data{'Danish'}\n"; #Access data with key
print "\$user_data{'Him'} = $user_data{'Him'}\n";
print "\$hash1{'key1'} = $hash1{'key1'}\n";

